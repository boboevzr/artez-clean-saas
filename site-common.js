  let _chatAudioCtx = null;
  function _chatSound(type) {
    try {
      if (!_chatAudioCtx) _chatAudioCtx = new (window.AudioContext || window.webkitAudioContext)();
      const ctx = _chatAudioCtx;
      const resume = fn => ctx.state === 'suspended' ? ctx.resume().then(fn) : fn();
      if (type === 'send') {
        resume(() => {
          const o = ctx.createOscillator(), g = ctx.createGain();
          o.connect(g); g.connect(ctx.destination);
          o.frequency.value = 440; o.type = 'sine';
          g.gain.setValueAtTime(0.18, ctx.currentTime);
          g.gain.exponentialRampToValueAtTime(0.001, ctx.currentTime + 0.1);
          o.start(ctx.currentTime); o.stop(ctx.currentTime + 0.12);
        });
      } else if (type === 'recv') {
        resume(() => {
          [[0, 880, 0.12], [0.14, 1100, 0.15]].forEach(([t, f, d]) => {
            const o = ctx.createOscillator(), g = ctx.createGain();
            o.connect(g); g.connect(ctx.destination);
            o.frequency.value = f; o.type = 'sine';
            g.gain.setValueAtTime(0.22, ctx.currentTime + t);
            g.gain.exponentialRampToValueAtTime(0.001, ctx.currentTime + t + d);
            o.start(ctx.currentTime + t); o.stop(ctx.currentTime + t + d + 0.05);
          });
        });
      }
    } catch(e) {}
  }
