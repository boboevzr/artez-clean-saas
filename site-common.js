  let _reviewsCarouselCleanup = null;
  function initReviewsCarousel() {
    if (_reviewsCarouselCleanup) _reviewsCarouselCleanup();
    const track    = document.getElementById('reviewsTrack');
    const dotsWrap = document.getElementById('revDots');
    if (!track) return;
    const cards = track.querySelectorAll('.review-card');
    if (!cards.length) return;
    let perView = window.innerWidth > 860 ? 3 : window.innerWidth > 560 ? 2 : 1;
    let cur = 0;
    let total = Math.ceil(cards.length / perView);

    dotsWrap.innerHTML = '';
    for (let i = 0; i < total; i++) {
      const d = document.createElement('div');
      d.className = 'rev-dot' + (i === 0 ? ' active' : '');
      d.addEventListener('click', () => goRev(i));
      dotsWrap.appendChild(d);
    }

    function goRev(idx) {
      cur = (idx + total) % total;
      const cardW = track.querySelector('.review-card').offsetWidth + 20;
      track.style.transform = `translateX(-${cur * perView * cardW}px)`;
      dotsWrap.querySelectorAll('.rev-dot').forEach((d,i) => d.classList.toggle('active', i === cur));
    }

    const prevBtn = document.getElementById('revPrev');
    const nextBtn = document.getElementById('revNext');
    const onPrev = () => goRev(cur - 1);
    const onNext = () => goRev(cur + 1);
    prevBtn?.addEventListener('click', onPrev);
    nextBtn?.addEventListener('click', onNext);

    let autoRev = setInterval(() => goRev(cur + 1), 5000);
    const onEnter = () => clearInterval(autoRev);
    const onLeave = () => { autoRev = setInterval(() => goRev(cur + 1), 5000); };
    track.addEventListener('mouseenter', onEnter);
    track.addEventListener('mouseleave', onLeave);

    const onResize = () => {
      const nv = window.innerWidth > 860 ? 3 : window.innerWidth > 560 ? 2 : 1;
      if (nv !== perView) { perView = nv; total = Math.ceil(cards.length / perView); cur = 0; goRev(0); }
    };
    window.addEventListener('resize', onResize);

    _reviewsCarouselCleanup = () => {
      clearInterval(autoRev);
      prevBtn?.removeEventListener('click', onPrev);
      nextBtn?.removeEventListener('click', onNext);
      track.removeEventListener('mouseenter', onEnter);
      track.removeEventListener('mouseleave', onLeave);
      window.removeEventListener('resize', onResize);
    };
  }

