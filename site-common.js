  function matchFilter(o){
    const s = o.status;
    if (currentFilter === 'all') return true;
    if (currentFilter === 'new') return s === 'new';
    if (currentFilter === 'progress') return PROGRESS_STATUSES.includes(s);
    if (currentFilter === 'done') return DONE_STATUSES.includes(s);
    if (currentFilter === 'cancelled') return s === 'cancelled';
    return true;
  }

