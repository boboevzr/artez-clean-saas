  function _chatConnectWs() {
    if (!_chatCode) return;
    // Закрываем старый сокет чтобы не было зомби-подключений
    if (_chatWs && _chatWs.readyState < 2) {
      _chatWs.onclose = null;
      _chatWs.close();
    }
    _chatWs = null;
    const wsBase = API_BASE.replace('https://','wss://').replace('http://','ws://').replace('/api','');
    const ws = new WebSocket(`${wsBase}/ws/chat/client/${_chatCode}`);
    _chatWs = ws;

    ws.onmessage = e => {
      const d = JSON.parse(e.data);
      if (d.type === 'history') {
        const box = document.getElementById('chatMessages');
        if (box) box.innerHTML = '';
        d.messages.forEach(m => _chatAddBubble(m.sender_type, m.sender_name, m.text, m.created_at));
        setTimeout(_chatScrollBottom, 50);
      } else if (d.type === 'message') {
        _chatAddBubble(d.msg.sender_type, d.msg.sender_name, d.msg.text, d.msg.created_at);
        if (d.msg.sender_type !== 'client') _chatSound('recv');
      } else if (d.type === 'staff_joined') {
        const st = document.getElementById('chatHeadStatus');
        if (st) { st.textContent = t('chat.statusOnline'); }
      } else if (d.type === 'chat_closed') {
        _chatMarkClosed();
      }
    };

    ws.onclose = () => {
      _chatWs = null;
      if (_chatCode) {
        // Переподключаемся всегда если есть активный код, независимо от _chatOpen
        setTimeout(() => { if (_chatCode) _chatConnectWs(); }, 5000);
      }
    };
  }

