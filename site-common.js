  function initYandexMap(){
    // Центр берём из координат выбранного филиала
    const cityCenter = getBranchCenter();

    function createMap(center, zoom){
      yandexMap = new ymaps.Map('orderMap', {
        center: center,
        zoom: zoom,
        controls: ['zoomControl']
      });
      yandexMarker = new ymaps.Placemark(center, {}, {
        draggable: true,
        preset: 'islands#redDotIcon'
      });
      yandexMap.geoObjects.add(yandexMarker);
      yandexMarker.events.add('dragend', () => {
        const c = yandexMarker.geometry.getCoordinates();
        _updateMapAddr(c[0], c[1]);
      });
      yandexMap.events.add('click', (e) => {
        const coords = e.get('coords');
        yandexMarker.geometry.setCoordinates(coords);
        _updateMapAddr(coords[0], coords[1]);
      });
    }

    // 1. Рисуем карту немедленно по центру города — без задержки
    createMap(cityCenter, 14);

    // 2. Фоново запрашиваем геолокацию: если пользователь ≤50 км — двигаем к нему
    if (navigator.geolocation){
      navigator.geolocation.getCurrentPosition(
        (pos) => {
          const userCoords = [pos.coords.latitude, pos.coords.longitude];
          if (distanceKm(userCoords, cityCenter) <= 50){
            yandexMap.setCenter(userCoords, 16);
            yandexMarker.geometry.setCoordinates(userCoords);
            _updateMapAddr(userCoords[0], userCoords[1]);
          }
        },
        () => { /* нет разрешения — карта уже показана по городу */ },
        { enableHighAccuracy: true, timeout: 6000, maximumAge: 60000 }
      );
    }
  }

