  function _buildAddr(d) {
    const a = d.address || {};
    const street   = a.road || a.pedestrian || a.footway || a.path || a.neighbourhood || a.quarter || a.residential || '';
    const house    = a.house_number || '';
    const street_h = [street, house].filter(Boolean).join(', ');
    const district = a.city_district || a.suburb || a.village_district || '';
    const city     = a.city || a.town || a.village || a.municipality || '';
    const region   = a.state || a.county || '';
    const parts    = [street_h, district, city, region].filter(Boolean);
    if (parts.length >= 2) return parts.join(', ');
    const dn = (d.display_name || '').split(',').map(s => s.trim()).filter(Boolean);
    return dn.filter(p => !['Узбекистан','Uzbekistan',"O'zbekiston"].includes(p)).slice(0, 5).join(', ');
  }

